# 边缘检测
# 原理
# 边缘检测是图像处理何计算机视觉中的基本问题，边缘检测的目的是标识0数字图像中亮度变化明显的点
# 图像属性中显著变化通常反映了属性的重要事件和变化
# 图像边缘检测大幅度的减少了数据量，并且剔除了可以认为不相关的信息，保留了图像重要的结构属性
# 有许多方法用于边缘检测，他们的绝大部分可以划分为两类：基于搜索和基于零穿越
# 基于搜索：通过寻找图像的一阶导数中的最大值来检测边界，然后利用计算结果估计边缘的局部方向，通常采用梯度的方向，并利用此方向找到局部梯度模的最大值
# 代表算法是Sobel算子和Scharr算子
# 基于零穿越：通过寻找图像的二阶导数零穿越来寻找边界
# 代表算法是Laplacian算子
